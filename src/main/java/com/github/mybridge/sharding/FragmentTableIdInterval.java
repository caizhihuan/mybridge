package com.github.mybridge.sharding;

public class FragmentTableIdInterval {
	private long fragmentTableId;
	private long startId;
	private long endId;

	public long getFragmentTableId() {
		return fragmentTableId;
	}

	public void setFragmentTableId(long fragmentTableId) {
		this.fragmentTableId = fragmentTableId;
	}

	public long getStartId() {
		return startId;
	}

	public void setStartId(long startId) {
		this.startId = startId;
	}

	public long getEndId() {
		return endId;
	}

	public void setEndId(long endId) {
		this.endId = endId;
	}
}
